
package MyFrames;


public class UserCheck {
    
  
    private String pname;
   
    private int mrp;
     
    
    
    public UserCheck(String Pname,int Mrp)
    {
       
        this.pname = Pname;
       
        this.mrp = Mrp;
        
    }
   
    
    public String getPname()
    {
        return pname;
    }
    
   
    
    public int getMrp()
    {
        return mrp;
    }
   
}


